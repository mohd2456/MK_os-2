LOGO_HEIGHT=${#LOGO[@]}
START_LINE=$(( (LINES - LOGO_HEIGHT) / 2 - 2 ))
for i in "${!LOGO[@]}"; do
    PAD=$(( (COLS - 46) / 2 ))
    tput cup $((START_LINE + i)) $PAD
    printf "${CYAN}${LOGO[$i]}${RESET}"
done
